package comparableandcomparator;
class Student implements Comparable<Student>{  
Integer rollno;  
String name;  
Integer age;  
Student(int rollno,String name,int age){  
this.rollno=rollno;  
this.name=name;  
this.age=age;  
}  
  
public int compareTo(Student st){  
if(age==st.age)  
return 0;  
else if(age>st.age)  
return 1;  
else  
return -1;  
}
}  